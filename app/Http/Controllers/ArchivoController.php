     */
    public function store(Request $request)
    {
       $datosArchivo = request()->all();
       $datosArchivo = request()->except('_token');
       if($request->hasFile('archivo')){
           $datosArchivo['archivo']=$request->file('archivo')->store('uploads','public'); 
       }
       Archivo::insert($datosArchivo);
       return redirect('archivos')->with('mensaje','Archivo agregado con exito');

    }

